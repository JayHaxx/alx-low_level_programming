#include <stdio.h>

/**
 * add - addition of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**i
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of integers
 */
int sub(int a , int b)
{      
	return (a - b);
}

/**
 * mod - divide two integers for remainder
 * @a: first integer
 * @b: second integer
 * Return: Remainder of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: product of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: Quotient of two integers
 */
int div(int a, int b)
{
	return (a / b);
}
